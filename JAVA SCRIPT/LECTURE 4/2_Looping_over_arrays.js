//                             LOOPING OVER AN ARRAY

// print all elements of an array using a loop


let heros = ["Ironman","Spiderman","Antman","Hulk","Captain America","Thor"];

for(let i = 0 ; i < heros.length ; i++){
    console.log(heros[i]);
}


// FOR OF LOOP

for(let el of heros){
    console.log(el);
}

